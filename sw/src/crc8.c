uint8_t crc8(uint8_t crc, const uint8_t *data, uint32_t len)
{
  while (len--) {
    crc = crc8_table[crc ^ *data++];
  }

  return crc;
}

uint8_t crc8_block(const uint8_t *data, uint32_t len)
{
  uint8_t crc = CRC8_INIT;

  while (len--) {
    crc = crc8_table[crc ^ *data++];
  }

  return crc;
}
